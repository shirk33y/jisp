use std::collections::HashMap;
use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use jisp_core::Syntax;

use super::{
    collect_fixture_files, discover_tests, escape_rust_string, fixture_path, sanitize_ident,
    unique_ident, FixtureTest, FixtureTestKind,
};

pub(super) fn generate(manifest_dir: &Path) -> Result<(), String> {
    let canonical_dir = manifest_dir.join("../../tests/language");
    let generated_dir = manifest_dir.join("../../tests/generated-language");
    let mut canonical = vec![];
    collect_fixture_files(&canonical_dir, &mut canonical, false, &[Syntax::Lisp])?;
    canonical.sort();

    let mut output = String::from("// @generated by crates/jisp-eval/build.rs\n\n");
    if canonical.is_empty() {
        return Err(
            "cross-syntax parity requires at least one canonical language fixture".to_owned(),
        );
    }

    let mut module_names = HashMap::new();
    for canonical_path in canonical {
        let canonical_source = fs::read_to_string(&canonical_path)
            .map_err(|error| format!("failed to read {}: {error}", canonical_path.display()))?;
        let canonical_fixture = fixture_path(&canonical_path)?;
        let tests = discover_tests(
            &canonical_path,
            &canonical_source,
            &["test", "test-error"],
            &canonical_fixture,
        )?;
        let relative = canonical_path
            .strip_prefix(&canonical_dir)
            .map_err(|error| {
                format!(
                    "failed to make canonical fixture {} relative to {}: {error}",
                    canonical_path.display(),
                    canonical_dir.display()
                )
            })?;
        let mut peers = vec![canonical_fixture.clone()];
        for (directory, extension) in [("json", "json"), ("yaml", "yaml"), ("ws", "ws")] {
            let peer = generated_dir
                .join(directory)
                .join(relative)
                .with_extension(extension);
            if !peer.is_file() {
                return Err(format!(
                    "{} is missing {} parity fixture {}",
                    canonical_fixture,
                    directory,
                    peer.display()
                ));
            }
            let source = fs::read_to_string(&peer)
                .map_err(|error| format!("failed to read {}: {error}", peer.display()))?;
            let peer_tests =
                discover_tests(&peer, &source, &["test", "test-error"], &canonical_fixture)?;
            validate_registry(&canonical_fixture, &tests, &peer, &peer_tests)?;
            peers.push(fixture_path(&peer)?);
        }

        let module = unique_ident(&mut module_names, &sanitize_ident(&canonical_fixture));
        output.push_str(&format!("mod {module} {{\n"));
        output.push_str("    use crate::portable_support::{ExpectedPortableTest, FixtureSource, PortableTestKind};\n\n");
        output.push_str("    const TESTS: &[ExpectedPortableTest] = &[\n");
        for test in &tests {
            let kind = match test.kind {
                FixtureTestKind::Assert => "PortableTestKind::Assert",
                FixtureTestKind::Error => "PortableTestKind::Error",
            };
            output.push_str(&format!(
                "        ExpectedPortableTest {{ id: \"{}\", name: \"{}\", kind: {kind} }},\n",
                escape_rust_string(&test.id),
                escape_rust_string(&test.name),
            ));
        }
        output.push_str("    ];\n\n");
        output.push_str("    #[test]\n    fn all_syntaxes_have_equal_outcomes() {\n");
        output.push_str("        crate::portable_support::assert_fixture_parity(\n");
        output.push_str(&format!(
            "            \"{}\",\n",
            escape_rust_string(&canonical_fixture)
        ));
        output.push_str("            &[\n");
        for path in peers {
            output.push_str(&format!(
                "                FixtureSource {{ file: \"{}\", source: include_str!(concat!(env!(\"CARGO_MANIFEST_DIR\"), \"/../../{}\")) }},\n",
                escape_rust_string(&path),
                escape_rust_string(&path),
            ));
        }
        output.push_str("            ],\n            TESTS,\n        );\n    }\n}\n\n");
    }

    reject_orphaned_peers(&canonical_dir, &generated_dir)?;

    let out_dir = PathBuf::from(
        env::var("OUT_DIR")
            .map_err(|error| format!("failed to read OUT_DIR for syntax parity tests: {error}"))?,
    );
    fs::write(out_dir.join("portable_syntax_parity_tests.rs"), output).map_err(|error| {
        format!("failed to write generated portable syntax parity tests: {error}")
    })?;
    Ok(())
}

fn validate_registry(
    canonical: &str,
    expected: &[FixtureTest],
    peer_path: &Path,
    actual: &[FixtureTest],
) -> Result<(), String> {
    if expected.len() != actual.len() {
        return Err(format!(
            "{canonical} and {} have different test counts: {} vs {}",
            peer_path.display(),
            expected.len(),
            actual.len()
        ));
    }
    for (index, (expected, actual)) in expected.iter().zip(actual).enumerate() {
        if expected.name != actual.name || expected.kind != actual.kind {
            return Err(format!(
                "{canonical} and {} diverge at test {index}: expected {} ({:?}), got {} ({:?})",
                peer_path.display(),
                expected.name,
                expected.kind as u8,
                actual.name,
                actual.kind as u8,
            ));
        }
    }
    Ok(())
}

fn reject_orphaned_peers(canonical_dir: &Path, generated_dir: &Path) -> Result<(), String> {
    for (directory, syntax) in [
        ("json", Syntax::Json),
        ("yaml", Syntax::Yaml),
        ("ws", Syntax::Ws),
    ] {
        let syntax_dir = generated_dir.join(directory);
        let mut generated = vec![];
        collect_fixture_files(&syntax_dir, &mut generated, true, &[syntax])?;
        for fixture in generated {
            let relative = fixture.strip_prefix(&syntax_dir).map_err(|error| {
                format!("{}: invalid generated fixture: {error}", fixture.display())
            })?;
            let canonical = canonical_dir.join(relative).with_extension("lisp");
            if !canonical.is_file() {
                return Err(format!(
                    "{} is an orphaned generated {} fixture; expected {}",
                    fixture.display(),
                    directory,
                    canonical.display()
                ));
            }
        }
    }
    Ok(())
}
