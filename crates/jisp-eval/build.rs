use std::collections::HashMap;
use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use jisp_core::{Node, SourceId, SyntaxParser};
use jisp_syntax_lisp::LispParser;

struct FixtureTest {
    index: usize,
    name: String,
    function: String,
}

fn main() {
    if let Err(error) = generate_tests() {
        panic!("{error}");
    }
}

fn generate_tests() -> Result<(), String> {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").map_err(|error| {
        format!("failed to read CARGO_MANIFEST_DIR for portable Lisp tests: {error}")
    })?);
    let fixtures_dir = manifest_dir.join("../../tests/language");
    println!("cargo:rerun-if-changed={}", fixtures_dir.display());

    let mut fixtures = fixture_files(&fixtures_dir)?;
    fixtures.sort();

    let mut output = String::new();
    output.push_str("// @generated by crates/jisp-eval/build.rs\n\n");

    if fixtures.is_empty() {
        output.push_str(
            "#[test]\nfn portable_lisp_fixtures_exist() {\n    panic!(\"no portable Lisp fixtures found in tests/language\");\n}\n",
        );
    }

    let mut module_names = HashMap::new();
    for path in fixtures {
        println!("cargo:rerun-if-changed={}", path.display());
        let source = fs::read_to_string(&path)
            .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
        let tests = discover_tests(&path, &source)?;
        let module = unique_ident(
            &mut module_names,
            &sanitize_ident(
                path.file_stem()
                    .and_then(|stem| stem.to_str())
                    .unwrap_or("fixture"),
            ),
        );
        let fixture_path = fixture_path(&path)?;

        output.push_str(&format!("mod {module} {{\n"));
        output.push_str(&format!(
            "    const FILE: &str = \"{}\";\n",
            escape_rust_string(&fixture_path)
        ));
        output.push_str(&format!(
            "    const SOURCE: &str = include_str!(concat!(env!(\"CARGO_MANIFEST_DIR\"), \"/../../{}\"));\n\n",
            escape_rust_string(&fixture_path)
        ));

        let mut function_names = HashMap::new();
        for test in tests {
            let function = unique_ident(&mut function_names, &test.function);
            output.push_str("    #[test]\n");
            output.push_str(&format!("    fn {function}() {{\n"));
            output.push_str(&format!(
                "        crate::portable_lisp_support::run_lisp_test(FILE, SOURCE, {}, \"{}\");\n",
                test.index,
                escape_rust_string(&test.name)
            ));
            output.push_str("    }\n\n");
        }

        output.push_str("}\n\n");
    }

    let out_dir = PathBuf::from(
        env::var("OUT_DIR")
            .map_err(|error| format!("failed to read OUT_DIR for portable Lisp tests: {error}"))?,
    );
    fs::write(out_dir.join("portable_lisp_tests.rs"), output)
        .map_err(|error| format!("failed to write generated portable Lisp tests: {error}"))?;

    Ok(())
}

fn fixture_files(fixtures_dir: &Path) -> Result<Vec<PathBuf>, String> {
    if !fixtures_dir.exists() {
        return Ok(vec![]);
    }

    let mut files = vec![];
    for entry in fs::read_dir(fixtures_dir)
        .map_err(|error| format!("failed to read {}: {error}", fixtures_dir.display()))?
    {
        let path = entry
            .map_err(|error| format!("failed to read fixture directory entry: {error}"))?
            .path();
        if path.extension().and_then(|extension| extension.to_str()) == Some("lisp") {
            files.push(path);
        }
    }
    Ok(files)
}

fn discover_tests(path: &Path, source: &str) -> Result<Vec<FixtureTest>, String> {
    let nodes = LispParser
        .parse_module(SourceId(0), source)
        .map_err(|error| format!("{}: parse failed: {error}", path.display()))?;
    let mut tests = vec![];

    for node in nodes {
        let Some(items) = node.as_form() else {
            continue;
        };
        if items.first().and_then(Node::as_symbol) != Some("test") {
            continue;
        }
        let name = items
            .get(1)
            .and_then(Node::as_string)
            .ok_or_else(|| format!("{}: test name must be a string", path.display()))?
            .to_owned();
        tests.push(FixtureTest {
            index: tests.len(),
            function: sanitize_ident(&name),
            name,
        });
    }

    if tests.is_empty() {
        return Err(format!(
            "{}: expected at least one top-level (test ...) form",
            path.display()
        ));
    }

    Ok(tests)
}

fn fixture_path(path: &Path) -> Result<String, String> {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").map_err(|error| {
        format!("failed to read CARGO_MANIFEST_DIR for portable Lisp tests: {error}")
    })?);
    let repo_root = manifest_dir.join("../..");
    path.strip_prefix(&repo_root)
        .map_err(|error| {
            format!(
                "failed to make fixture path relative to {}: {error}",
                repo_root.display()
            )
        })
        .map(|path| path.to_string_lossy().replace('\\', "/"))
}

fn sanitize_ident(input: &str) -> String {
    let mut ident = String::new();
    let mut last_was_underscore = false;

    for character in input.chars() {
        if character.is_ascii_alphanumeric() {
            ident.push(character.to_ascii_lowercase());
            last_was_underscore = false;
        } else if !last_was_underscore {
            ident.push('_');
            last_was_underscore = true;
        }
    }

    let ident = ident.trim_matches('_');
    let mut ident = if ident.is_empty() {
        "test".to_owned()
    } else {
        ident.to_owned()
    };

    if ident
        .chars()
        .next()
        .is_some_and(|character| character.is_ascii_digit())
        || is_rust_keyword(&ident)
    {
        ident.insert_str(0, "test_");
    }

    ident
}

fn unique_ident(names: &mut HashMap<String, usize>, ident: &str) -> String {
    let count = names.entry(ident.to_owned()).or_insert(0);
    *count += 1;
    if *count == 1 {
        ident.to_owned()
    } else {
        format!("{ident}_{count}")
    }
}

fn is_rust_keyword(ident: &str) -> bool {
    matches!(
        ident,
        "as" | "break"
            | "const"
            | "continue"
            | "crate"
            | "else"
            | "enum"
            | "extern"
            | "false"
            | "fn"
            | "for"
            | "if"
            | "impl"
            | "in"
            | "let"
            | "loop"
            | "match"
            | "mod"
            | "move"
            | "mut"
            | "pub"
            | "ref"
            | "return"
            | "self"
            | "Self"
            | "static"
            | "struct"
            | "super"
            | "trait"
            | "true"
            | "type"
            | "unsafe"
            | "use"
            | "where"
            | "while"
            | "async"
            | "await"
            | "dyn"
    )
}

fn escape_rust_string(value: &str) -> String {
    value.escape_default().to_string()
}
